  const imageBlock = document.getElementById('center');
const searchBar = document.getElementById('search');

const showStates = (data) => {
  data.forEach((item, index) => {
    if (index % 2 === 0) {
      // Start a new row for every even-indexed item
      imageBlock.innerHTML += '<div class="row">';
    }

    imageBlock.innerHTML += `<div class="stateBlock">
      <img src="${item.stateImage}" />
      <h4 class="state_name">${item.stateName}</h4>
      <div class="description">
        <h4>${item.stateName}</h4>
        <p>${item.stateDescription}</p>
        <a href="${item.stateInfo}" target="_blank">VIEW IN 360</a>
      </div>
    </div>`;

    if (index % 2 === 1 || index === data.length - 1) {
      // Close the row for every odd-indexed item or the last item
      imageBlock.innerHTML += '</div>';
    }
  });
};

